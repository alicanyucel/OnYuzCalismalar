sehirler.push("ankara")
//concat iki diziyi birleştirip sonucu verir diziye ekleme yapmaz
sehirler.concat(["van","muş"])
//sort ise a -zye sıralar
sehirler.sort()
//lenght ise dizinin uzunluğunu verir eleman sayısı
sehirler.length()
